                                `city_name` varchar(255) DEFAULT NULL COMMENT '城市名称',
                                `province_id` bigint(20) DEFAULT NULL COMMENT '省份id',
                                `province_name` varchar(255) DEFAULT NULL COMMENT '省份名称',
                                `industry` int(11) DEFAULT NULL COMMENT '产业',
